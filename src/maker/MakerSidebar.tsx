export function MakerSidebar() {
  const { pathname } = useLocation()
  return (
    <aside className="hidden md:block w-60 flex-shrink-0">
      <nav className="sticky top-20 space-y-6">
        {MAKER_NAV.map((group) => (
          <div key={group.label}>
            <p className="text-[11px] font-bold text-foreground-faint uppercase tracking-wider px-3 mb-2">
              {group.label}
            </p>
            <ul className="space-y-0.5">
              {group.items.map((item) => {
                const active = pathname === item.to || (item.to !== '/dashboard' && pathname.startsWith(item.to))
                return (
                  <li key={item.to}>
                    <Link
                      to={item.to}
                      className={`flex items-center gap-2.5 px-3 py-2 rounded-button text-sm font-medium transition-colors ${
                        active
                          ? 'bg-primary-muted text-primary'
                          : 'text-foreground-muted hover:bg-surface-raised hover:text-foreground'
                      }`}
                    >
                      <Icon kind={item.icon} />
                      {item.label}
                    </Link>
                  </li>
                )
              })}
            </ul>
          </div>
        ))}
      </nav>
    </aside>
  )
}
